/**
 * Deterministic city → slug.
 * The first city of each province is the capital and gets the province slug
 * itself (e.g. `tehran-tehran` → `tehran`) for clean URLs.
 */
export function cityToSlug(provinceSlug: string, cityName: string, isCapital = false): string {
  if (isCapital) return provinceSlug;
  const persianMap: Record<string, string> = {
    ا: 'a',
    آ: 'a',
    أ: 'a',
    إ: 'a',
    ب: 'b',
    پ: 'p',
    ت: 't',
    ث: 's',
    ج: 'j',
    چ: 'ch',
    ح: 'h',
    خ: 'kh',
    د: 'd',
    ذ: 'z',
    ر: 'r',
    ز: 'z',
    ژ: 'zh',
    س: 's',
    ش: 'sh',
    ص: 's',
    ض: 'z',
    ط: 't',
    ظ: 'z',
    ع: 'a',
    غ: 'gh',
    ف: 'f',
    ق: 'gh',
    ک: 'k',
    ك: 'k',
    گ: 'g',
    ل: 'l',
    م: 'm',
    ن: 'n',
    و: 'v',
    ه: 'h',
    ی: 'i',
    ي: 'i',
    ى: 'i',
    ة: 'h',
    '‌': '-',
    ' ': '-',
  };
  const transliterated = cityName
    .split('')
    .map((ch) => persianMap[ch] ?? '')
    .join('')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
  return `${provinceSlug}-${transliterated || 'city'}`;
}
